/* Q1.Display numbers from 1 to 100 .*/

class Display
{
	public static void main(String[] args)
	{
		int n = 1;
		System.out.print("\n\t\t\tDisplaying number from 1 to 100");
		System.out.println("\n\t\t\t================================");

		while(n<=100)
		{
			System.out.print(+n+" ");
			n++;
		}
	}
}